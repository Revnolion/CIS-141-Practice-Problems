# Create a program that prompts for 2 numbers and then outputs the addition, subtraction, multiplication, and division of the first number by the second number. 
first_number = int(input("What is the first number? "))
second_number = int(input("What is the second number? "))

total_addition = first_number + second_number
total_subtraction = first_number - second_number
total_multiply = first_number * second_number
total_divide = first_number / second_number

print(total_addition)
print(total_subtraction)
print(total_multiply)
print(total_divide)
